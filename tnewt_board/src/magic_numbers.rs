// Does not include pawns
pub fn get_threat_pieces(direction_index: usize) -> Result<&'static [PieceKind], board::Error> {
    match direction_index {
        0..=3 => Ok(&[
            PieceKind::Rook,
            PieceKind::Queen,
        ]),
        4..=7 => Ok(&[
            PieceKind::Bishop,
            PieceKind::Queen,
        ]),
        _ => Err(board::Error::InvalidDirectionIndex(direction_index))
    }
}

